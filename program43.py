#--write a program to print 1 to 5 number using for loop--#

for i in range(1 ,6):
    print(i)
print("End of the program")


for i in range(6):
    print(i)
print("End of the program")


for i in range(1,10, 2):
    print(i)
print("End of the program")


for i in range(5 ,0, -1):
    print(i)
