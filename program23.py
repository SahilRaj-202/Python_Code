#--write a program to calculate the area of rectangle and perimeter--#
Length=eval(input('Enter The Length of Rectangle :'))
Breadth=eval(input('Enter The Breadth of Rectangle :'))
print("------------------------------------------")
print("Length of Rectangle :",Length)
print("Breadth of Rectangle :",Breadth)
print("---------------------Area of Rectangle---------------------")
Area=Length*Breadth
perimeter=2*Length*Breadth;
print('Area of Rectangle is ',Area)
print('Area of Perimeter is ',perimeter)

