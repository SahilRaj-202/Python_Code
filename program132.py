# ---------------------------------Union SET OPERATIONS-------------------------------------
S1={1,2,3,4,5}
S2={6,7,8,9,10}
print('Union method')
print('The union of two set is \n',S1.union(S2))

print('\n')

# ---------------------------------Intersection SET OPERATIONS-------------------------------------
S3={1,2,3,4,5}
S4={6,7,4,3,2,1}
print('Intersection method')
print('The intersection of two set is \n',S3.intersection(S4))

print('\n')

# ---------------------------------Difference SET OPERATIONS-------------------------------------
S5={1,2,3,4,5}
S6={6,7,4,3,2,1}
print('Difference method')
print('The Difference of two set is \n',S5.difference(S6))

print('\n')


# ---------------------------------Symmetric_Difference SET OPERATIONS-------------------------------------
S7={1,2,3,4,}
S8={4,5,6}
print('Symmetric_Difference method')
print('The Symmetric_Difference of two set is \n',S7.symmetric_difference(S8))

print('\n')